""" Given a list of integers, write a Python script that uses a for loop and if-else statements to print each number followed by "is even" if the number is even, and "is odd"
if the number is odd """
numList=[1,3,7,9,12,10]  
for i in numList:
    if i%2 ==0:
        print(f"{i} is even")
        
    else:
        print(f"{i} is odd")    
        