""" Write a Python program that uses a for loop to iterate over a list of numbers and uses an if-else statement to count
 how many of these numbers are positive. """
noList=[1,-1,2,8,-9,-7]
count=0

for i in noList:
    if i>0:
      count=count+1      
print (f" Number of element greater than zero :{count}")  


